
#ifndef _TYPEDEF_H_
    #define _TYPEDEF_H_

#ifndef UINT8
    #define UINT8  unsigned char
#endif

#ifndef INT8
    #define INT8   signed char
#endif

#ifndef UINT16
    #define UINT16 unsigned short int
#endif

#ifndef INT16
    #define INT16  signed short int
#endif

#ifndef UINT32
    #define UINT32 unsigned int
#endif

#ifndef INT32
    #define INT32  int
#endif

#ifndef INT64
    #define INT64  __int64
#endif

#ifndef UINT64
    #define UINT64 unsigned __int64
#endif

#ifndef CHAR
    #define CHAR  char
#endif

#ifndef FLOAT
    #define FLOAT  float
#endif

#ifndef DOUBLE
    #define DOUBLE double
#endif

#ifndef BOOL
    #define BOOL  INT8
#endif

#ifndef TRUE
    #define TRUE  (1)
#endif

#ifndef FALSE
    #define FALSE (0)
#endif

#ifndef NULL
    #define NULL  (0)
#endif

#ifndef BYTE
    #define BYTE    CHAR
#endif




#endif // _TYPEDEF_H_
